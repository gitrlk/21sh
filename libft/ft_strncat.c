#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	s1l;
	size_t	c;

	c = 0;
	s1l = (ft_strlen(s1));
	while (c < n && s2[c])
	{
		s1[s1l] = s2[c];
		s1l++;
		c++;
	}
	s1[s1l] = '\0';
	return (s1);
}
