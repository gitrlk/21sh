#include "libft.h"

size_t	ft_strcspn(const char *s1, char s2)
{
	int	i;

	i = 0;
	if (s1 && s2)
	{
		while (s1[i] != '\0' && s2 != '\0' && s1[i] != s2)
			i++;
	}
	return (i);
}
